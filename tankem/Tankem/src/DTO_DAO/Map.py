class Map():
	def __init__(self, id, titre, colonnes, rangees, dateCreation, status, respawnMin, respawnMax):
		self.id = id
		self.titre = titre
		self.colonnes = colonnes
		self.rangees = rangees
		self.dateCreation = dateCreation
		self.status = status
		self.respawnMin = respawnMin
		self.respawnMax = respawnMax